    void loadAccount() throws IOException {
        ArrayList<String> a = new Xuly(socket, in, out).GetAllTaikhoan();

        Vector cols = new Vector();
        cols.addElement("MãTK");
        cols.addElement("Tên đăng nhập");
        cols.addElement("Tên đầy đủ");
        cols.addElement("Ngày sinh");
        cols.addElement("Giới tính");
        cols.addElement("Lớp học");
        cols.addElement("Quyền");

        Vector data = new Vector();
        for (String s : a) {
            Vector u = new Vector();
            String[] ss = s.split(";");
            u.addElement(ss[0]);
            u.addElement(ss[1]);
            u.addElement(ss[2]);
            u.addElement(ss[3]);
            u.addElement(ss[4].equals("true") ? "Nam" : "Nữ");
            u.addElement(ss[5]);
            u.addElement(ss[6].equals("1") ? "Admin" : ss[6].equals("2") ? "Giảng viên" : "Sinh viên");
            data.add(u);
        }
        jTable1.setModel(new DefaultTableModel(data, cols));

    }

